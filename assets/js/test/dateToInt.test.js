const dateToInt = require('./dateToInt');

const inputArr = [0, "14/05/2021 11:13:21", "17/05/2021 16:40:08", "17/05/2021 16:40:10", "17/05/2021 16:40:28"];

// e.g. "14/05/2021 11:13:21"
// 
// 14*86400 = 1209600
// 5*2628288 = 13141440
// 2021*31557600 = 63777909600
// 11*3600 = 39600
// 13*60 = 780
// 21 = 21
// 
// The sum = 63792301041
//                        
// "14/05/2021 11:13:21" == 63792301041


test('dates to integers', () => {
    expect(dateToInt(inputArr)).toEqual([undefined, 63792301041, 63792579848, 63792579850, 63792579868]);
});